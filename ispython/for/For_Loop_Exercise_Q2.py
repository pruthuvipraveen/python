#Print the odd numbers from 1 to 20 using a for loop.

print ("All Odd numbers from 1 to 20 : \n")
for i in range (1,20):
    if i % 2 != 0:
        print (i)

print ("\nAll even numbers from 1 to 20 : \n")
for i in range (1,21):
    if i % 2 == 0:
        print (i)
